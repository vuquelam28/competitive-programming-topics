#include <bits/stdc++.h>

using namespace std;

main()
{
    int a, b;
    cin >> a >> b;
    
    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << fixed << setprecision(1) << (double) a / b << endl; 
    cout << a / b << endl;
    cout << a % b;
   
    return 0;
}